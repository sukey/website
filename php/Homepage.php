<p>Sukey is a web app that is designed to keep people safe, mobile and informed during demonstrations. We crowdsource updates from twitter and other online and offline sources in order to provide our users with a timely overview of what is going on at a demonstration. We also provide our users with a dynamically updated map view (now using open source maps) and the ability to anonymously send us photo and text reports updates through the app itself (we delete exif data, timestamps and filenames and we dont keep server logs).</p>
